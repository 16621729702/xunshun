package com.wink.livemall.member.dto;


import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import java.math.BigDecimal;
import java.util.Date;
@Table(name = "lm_member_coupon")
public class LmMemberCoupon {
  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY,generator = "JDBC")
  private int id;
  private int coupon_id;
  private java.util.Date create_time;
  private int merch_id;
  private java.util.Date start_time;
  private java.util.Date end_tiem;
  private String comment;
  private BigDecimal pirce;
  private int state;
  private int member_id;
  private BigDecimal startprice;
  public LmMemberCoupon() {
  }

  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }

  public int getCoupon_id() {
    return coupon_id;
  }

  public void setCoupon_id(int coupon_id) {
    this.coupon_id = coupon_id;
  }

  public Date getCreate_time() {
    return create_time;
  }

  public void setCreate_time(Date create_time) {
    this.create_time = create_time;
  }

  public int getMerch_id() {
    return merch_id;
  }

  public void setMerch_id(int merch_id) {
    this.merch_id = merch_id;
  }

  public Date getStart_time() {
    return start_time;
  }

  public void setStart_time(Date start_time) {
    this.start_time = start_time;
  }

  public Date getEnd_tiem() {
    return end_tiem;
  }

  public void setEnd_tiem(Date end_tiem) {
    this.end_tiem = end_tiem;
  }

  public String getComment() {
    return comment;
  }

  public void setComment(String comment) {
    this.comment = comment;
  }

  public BigDecimal getPirce() {
    return pirce;
  }

  public void setPirce(BigDecimal pirce) {
    this.pirce = pirce;
  }

  public int getState() {
    return state;
  }

  public void setState(int state) {
    this.state = state;
  }

  public int getMember_id() {
    return member_id;
  }

  public void setMember_id(int member_id) {
    this.member_id = member_id;
  }

  public BigDecimal getStartprice() {
    return startprice;
  }

  public void setStartprice(BigDecimal startprice) {
    this.startprice = startprice;
  }
}
