package com.wink.livemall.sys.image.dto;

import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;
import java.util.Date;

@Table(name="lm_image_core")
public class LmImageCore {
  @Id
  @GeneratedValue(strategy = GenerationType.IDENTITY,generator = "JDBC")
  private int id;
  private int uniacid;
  private int uid;
  private String filename;
  private String attachment;
  private int type;
  private Date createtime;
  private String module_upload_dir;
  private int group_id;
  private int uploadtype;
  private String imgurl;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public int getUniacid() {
    return uniacid;
  }

  public void setUniacid(int uniacid) {
    this.uniacid = uniacid;
  }


  public int getUid() {
    return uid;
  }

  public void setUid(int uid) {
    this.uid = uid;
  }


  public String getFilename() {
    return filename;
  }

  public void setFilename(String filename) {
    this.filename = filename;
  }


  public String getAttachment() {
    return attachment;
  }

  public void setAttachment(String attachment) {
    this.attachment = attachment;
  }


  public int getType() {
    return type;
  }

  public void setType(int type) {
    this.type = type;
  }


  public Date getCreatetime() {
    return createtime;
  }

  public void setCreatetime(Date createtime) {
    this.createtime = createtime;
  }

  public String getModule_upload_dir() {
    return module_upload_dir;
  }

  public void setModule_upload_dir(String module_upload_dir) {
    this.module_upload_dir = module_upload_dir;
  }

  public int getGroup_id() {
    return group_id;
  }

  public void setGroup_id(int group_id) {
    this.group_id = group_id;
  }

  public int getUploadtype() {
    return uploadtype;
  }

  public void setUploadtype(int uploadtype) {
    this.uploadtype = uploadtype;
  }

  public String getImgurl() {
    return imgurl;
  }

  public void setImgurl(String imgurl) {
    this.imgurl = imgurl;
  }
}
